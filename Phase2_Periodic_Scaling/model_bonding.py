import torch
import torch.nn as nn
from model_atomic import AtomicWhitebox

class CouplingWhitebox(nn.Module):
    def __init__(self):
        super(CouplingWhitebox, self).__init__()
        
        # We instantiate a SINGLE atomic node serving as a shared-weight Siamese twin.
        # This guarantees physical laws are identical for all atoms.
        self.atomic_node = AtomicWhitebox()
        
        # The ultimate universal constant we analytically discovered:
        # The true Elasticity Modulus of the continuous universe mathematically 
        # locked to the 3D-to-1D spherical projection ratio (1 / sqrt(pi))
        import math
        self.register_buffer('vacuum_alpha', torch.tensor([1.0 / math.sqrt(math.pi)], dtype=torch.float32))
        
        # Mathematical projection constraint (Parallax of Time)
        self.projection_constant = 0.529 # Bohr constant anchoring arbitrary grid space to Angstroms

    def evaluate_4d_overlap(self, d, S_A, P_A, D_A, S_B, P_B, D_B, X, Y, Z, W, alpha):
        """
        Calculates the continuous topological drag across raw 4D Euclidean space (e1, e2, e3, e4).
        """
        # Positional offsets across the primary collision axis (e.g. X-axis, e1)
        X_A = X + (d / 2.0)
        X_B = X - (d / 2.0)
        
        # 4D Absolute Radius from both structural centers
        r_A = torch.sqrt(X_A**2 + Y**2 + Z**2 + W**2)
        r_B = torch.sqrt(X_B**2 + Y**2 + Z**2 + W**2)
        
        # Bounded geometric expansions (Unscaled topological footprints)
        env_A = torch.exp(- (r_A * alpha) / S_A)
        env_B = torch.exp(- (r_B * alpha) / S_B)
        
        # Spatial Overlap Integration
        overlap = env_A * env_B
        
        # Topological Attraction (Driven by the Chiral Polarity of the multivectors interlocking)
        attraction = (P_A * P_B) * overlap
        
        # Core Repulsion (Driven by Core Density resisting identical spatial occupation, squared curve)
        repulsion = (D_A * D_B) * (overlap ** 2)
        
        # Total topological tension potential (The Well)
        # We want to MINIMIZE this potential. SoftArgmin will naturally find the lowest state.
        well_potential = repulsion - attraction
        
        return torch.sum(well_potential)

    def forward(self, atom_A_params, atom_B_params):
        S_A, P_A, D_A = self.atomic_node(atom_A_params)
        S_B, P_B, D_B = self.atomic_node(atom_B_params)
        
        # 4D Meshgrid - Lower resolution to test Cuda scaling (20x20x20x20 = 160,000 blocks per frame)
        grid_vals = torch.linspace(-6.0, 6.0, 20, device=atom_A_params.device)
        # Using meshgrid to generate full 4-dimensional volumetric collision tensor
        W, Z, Y, X = torch.meshgrid(grid_vals, grid_vals, grid_vals, grid_vals, indexing='ij')
        
        # Evaluating the well mapping across explicit physical distances
        distances = torch.linspace(0.1, 7.0, 40, device=atom_A_params.device)
        
        drags = []
        for d in distances:
            drag = self.evaluate_4d_overlap(d, S_A, P_A, D_A, S_B, P_B, D_B, X, Y, Z, W, self.vacuum_alpha)
            drags.append(drag)
            
        drags = torch.stack(drags)
        
        # SoftArgmin: Differentiable trick to find the distance with the LOWEST drag.
        # NORMALIZE the drags first so that the well scale is isolated relative to the specific atoms!
        # If the drag is massive (10^6) or tiny (10^-6), the neural gradient dies. Normalization keeps the well curve alive.
        drag_min = torch.min(drags)
        drag_max = torch.max(drags)
        norm_drags = (drags - drag_min) / (drag_max - drag_min + 1e-8)
        
        tau = 0.1 # Sharp temperature to isolate the actual minimum
        weights = torch.softmax(-norm_drags / tau, dim=0)
        
        lock_distance_4D = torch.sum(weights * distances)
        
        lock_distance_3D = lock_distance_4D * self.projection_constant
        return lock_distance_3D

